package com.c4po.pattern.product;
/**
 * 抽象茶产品
 * @author Lisa
 * @date 2018/9/15
 */

public abstract class AbstractTeaProduct   {

    /**
     * 茶汤颜色
     */
    public abstract void teaColor();

    /**
     * 干茶外形
     */
    public abstract void dryTeaShape();

    /**
     * 茶香
     */
    public abstract void teaFlavor();

}

